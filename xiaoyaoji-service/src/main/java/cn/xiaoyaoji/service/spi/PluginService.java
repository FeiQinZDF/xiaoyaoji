public interface PluginService<T> {

    /**
     * 注册插件
     *
     * @param pluginId 插件id
     * @param plugin   插件类
     * @return T
     */
    T register(String pluginId, T plugin);

    /**
     * 注销插件
     *
     * @param pluginId 插件id
     * @return true/false
     */
    T destroy(String pluginId);

    /**
     * 获取插件类
     *
     * @param pluginId 插件id
     * @return T
     */
    T getPlugin(String pluginId);
}
